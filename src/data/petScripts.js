/**
 * Get a random script line for a pet, topic, and type ('opening' | 'followup').
 * Returns null if not found.
 */
export function getScript(petId, topic, type) {
  const arr = PET_SCRIPTS[petId]?.[topic]?.[type];
  if (!arr?.length) return null;
  return arr[Math.floor(Math.random() * arr.length)];
}
