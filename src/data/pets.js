/**
 * Returns today's pet (deterministic per calendar day).
 * Only cycles through pets that have full image assets.
 */
export function getTodaysPet() {
  const today = new Date();
  const dayOfYear = Math.floor((today - new Date(today.getFullYear(), 0, 0)) / 86400000);
  const petIds = Object.keys(PETS);
  return PETS[petIds[dayOfYear % petIds.length]];
}

export function getPetById(id) {
  return PETS[id] || PETS.sheep;
}

